using System;

namespace MyMath
{
    /// <summary>
    /// Provides a collection of math operations
    /// </summary>
    class Operations
    {
        /// <summary>
        /// Adds two integers together
        /// </summary>
        /// <returns>The sum of two integers</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
